	T2MOD SET 0C9H
	T2CON SET 0C8H
	TH2 SET 0CDH
	TL2 SET 0CCH
	RCAP2H SET 0CBH
	RCAP2L SET 0CAH
	
	LJMP INIT

	ORG 003H
	LJMP ZERT0
	
	ORG 00BH
	LJMP TIN0
	
	ORG 02BH
	LJMP IRQT2

	ORG 50H
INIT:
	MOV R7,#10D
	MOV P3,#0FFH
	MOV R6,#0
	MOV RCAP2H,#03CH
	MOV RCAP2L,#0AFH
	MOV TH2,#03CH
	MOV TL2,#0AFH
	MOV T2MOD,#0
	MOV T2CON,#00000100B
	
	MOV TMOD,#00001100B
	MOV TCON,#00010001B
	SETB IE.0
	SETB IE.1
	SETB IE.5
	SETB IE.7
	LJMP MAIN
	
	ORG 100H
MAIN:
	INC R6
	MOV A,TH0
	MOV P2,TL0
	LJMP MAIN
	
	ORG 200H
IRQT2:
	DJNZ R7,END_IRQT2
	MOV R7,#10D
	CPL P1.0
	INC R4
END_IRQT2:
	RETI
	
TIN0:
	RETI
ZERT0:
	INC R5
	MOV TH0,#0
	MOV TL0,#0
	RETI
END